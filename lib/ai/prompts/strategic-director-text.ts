/**
 * 生成用户提示词
 */
export function generateTextStrategyPrompt(
  profileData: any,
  diagnosisData: any,
  bio: string
): string {
  const { username, full_name, follower_count, post_count, activity_status, category_label } = profileData
  const { score, summary_title, key_issues } = diagnosisData

  return `
请为以下Instagram账号生成完整的运营策划案:

账号基本信息:
- 用户名: @${username}
- 全名: ${full_name}
- 行业类别: ${category_label || '本地商家'}
- 粉丝数: ${follower_count?.toLocaleString() || 'N/A'}
- 帖子数: ${post_count || 'N/A'}
- 活跃状态: ${activity_status}
- Bio: ${bio || '(空)'}

诊断结果:
- 健康评分: ${score}/100
- 总体评价: ${summary_title}
- 关键问题:
${key_issues?.map((issue: string, i: number) => `  ${i + 1}. ${issue}`).join('\n') || '  无'}

任务要求:
1. 生成一份7000-10000字的完整策划案
2. 包含7个核心模块(账号背景、对标分析、用户画像、人设设计、内容栏目、发布排期、数据运营)
3. 输出纯文本格式,不使用任何特殊标记符号
4. 所有内容必须基于上述真实数据
5. 语言专业但易懂,所有建议必须具体可执行

请按照System Prompt中的结构生成策划案!
`
}
